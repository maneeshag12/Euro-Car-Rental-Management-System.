    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed

        Connection conn;
        PreparedStatement ps;

        try {
            Class.forName("com.mysql.jdbc.Driver");
            conn = DriverManager.getConnection("jdbc:mysql://localhost:3308/euro_db", "root", "");
            String sql = "select * from user where username=? and password=? and userStatus='Available'";
            ps = conn.prepareStatement(sql);
            ps.setString(1, txtUsername.getText());
            ps.setString(2, txtPassword.getText());
            ResultSet rs = ps.executeQuery();
            
            if (rs.next())
            {
                String usertype = rs.getString("usertype");
                GloUsername = rs.getString("displayName");
                if (usertype.equals("Admin"))
                {
                    this.setVisible(false);
                    AdminPanelUI apUI = new AdminPanelUI();
                    apUI.setVisible(true);
                    //break;
                }
                else
                {
                    this.setVisible(false);
                    MainUI main = new MainUI();
                    main.setVisible(true);
                    //break;
                }
            }
            else
            {
                conn.close();
                JOptionPane.showMessageDialog(null, "Incorrect Login Credentials");
            }
        }
        catch (Exception e)
        {
            JOptionPane.showMessageDialog(null, e);
        }
        
        
    }//GEN-LAST:event_btnLoginActionPerformed

